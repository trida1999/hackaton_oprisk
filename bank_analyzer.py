def analyze_bank_reviews(question: str):
    """–ó–∞–ø—É—Å–∫–∞–µ—Ç –º—É–ª—å—Ç–∏–∞–≥–µ–Ω—Ç–Ω—ã–π –∞–Ω–∞–ª–∏–∑ —Å –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å—é –¥–æ—Ä–∞–±–æ—Ç–æ–∫"""
    max_revisions = 2  # –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –¥–æ—Ä–∞–±–æ—Ç–æ–∫
    current_revision = 0
    approved = False
    final_result = None
    critique_feedback = ""

    while not approved and current_revision < max_revisions:
        print(f"\nüîß –ò—Ç–µ—Ä–∞—Ü–∏—è –¥–æ—Ä–∞–±–æ—Ç–∫–∏ {current_revision + 1}/{max_revisions}")

        # –°–æ–∑–¥–∞–µ–º/–æ–±–Ω–æ–≤–ª—è–µ–º –∑–∞–¥–∞—á–∏
        tasks = create_analysis_tasks(question)

        if current_revision > 0:
            # –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–º–µ—á–∞–Ω–∏—è –≤ –∑–∞–¥–∞—á—É report_builder
            tasks[3].description += f"\n\n–ó–ê–ú–ï–ß–ê–ù–ò–Ø –ö –î–û–†–ê–ë–û–¢–ö–ï:\n{critique_feedback}"

        # –ó–∞–ø—É—Å–∫–∞–µ–º crew
        crew = Crew(
            agents=[senior_analyst, risk_assistant, insights_agent, report_builder, critic],
            tasks=tasks,
            process=Process.sequential
        )

        result = crew.kickoff()

        # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∫—Ä–∏—Ç–∏–∫–∞ (–ø–æ—Å–ª–µ–¥–Ω—è—è –∑–∞–¥–∞—á–∞)
        if "APPROVED" in str(result).split("\n")[-1]:
            approved = True
            final_result = result
        else:
            critique_feedback = str(result).split("\n")[-1]
            current_revision += 1
            if current_revision < max_revisions:
                print("üîÑ –û—Ç–ø—Ä–∞–≤–∫–∞ –Ω–∞ –¥–æ—Ä–∞–±–æ—Ç–∫—É...")

    if not approved:
        final_result = str(result) + "\n\n‚ö†Ô∏è –î–æ—Å—Ç–∏–≥–Ω—É—Ç –ª–∏–º–∏—Ç –¥–æ—Ä–∞–±–æ—Ç–æ–∫. –û—Ç—á–µ—Ç –ø—Ä–∏–Ω—è—Ç —Å –∑–∞–º–µ—á–∞–Ω–∏—è–º–∏."

    return final_result




